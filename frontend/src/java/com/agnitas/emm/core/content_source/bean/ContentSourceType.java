package com.agnitas.emm.core.content_source.bean;

import com.agnitas.beans.IntEnum;

public enum ContentSourceType implements IntEnum {

    XML(1, "XML"), HTML(2, "HTML");

    private int id;
    private String name;

    ContentSourceType(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int getId() {
        return id;
    }

    public static ContentSourceType fromId(int id) {
        return IntEnum.fromId(ContentSourceType.class, id);
    }
}
