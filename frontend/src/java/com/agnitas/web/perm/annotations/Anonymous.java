package com.agnitas.web.perm.annotations;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Makes an annotated handler method or all handler methods of an annotated controller available to non-authenticated (anonymous) users.
 * All the controllers and handler methods require user to be authenticated by default.
 */
@Target({ElementType.METHOD, ElementType.TYPE})
@Retention(RetentionPolicy.RUNTIME)
public @interface Anonymous {
	// nothing to do
}
