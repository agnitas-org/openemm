package org.agnitas.beans.impl;
/**
 * Small bean which keeps a key and a value. Useful for e.g. html options where you need just a slim object.
 * Have a look at Map.Entry
 */
public class SimpleKeyValueBean {
	String key;
	String value;
	
	public SimpleKeyValueBean(String key, String value) {
		this.key = key;
		this.value = value;
	}
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
}
